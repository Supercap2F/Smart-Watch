void init_io_ports(void) {
    /*******************************************************************/
    /* Section: I/O pin configuration                                  */
    /*******************************************************************/
    /*
     * I/O connections:                            
     ***********************************************************************
     *                              PORTB  
     * RB15 -> Dis_D/C | RB14 -> FTDI_RST | RB13 <- N/C      | RB12 <- N/C
     * RB11 <- CBUS2   | RB10 <- CBUS4    | RB9  <- N/C      | RB8  <- N/C
     * RB7  -> Motor   | RB6  <-a BatIn   | RB5  <- N/C      | RB4  <- N/C
     * RB3  <- N/C     | RB2  <- N/C      | RB1  <- PGC      | RB0  <- PGD
     */
    ANSELB = 0b0011001101111100;
    TRISB  = 0b0011111101111111;
    LATB   = 0b0000000000000000; 
    
    /***********************************************************************
     *                              PORTC 
     * RC15 <- OSC2    | RC14 <- SOSC0    | RC13 <- SOSC1    | RC12 <- OSC1
     */
    ANSELC = 0b1111000000000000;
    TRISC  = 0b1111000000000000;
    LATC   = 0b0000000000000000;
    
    /**********************************************************************
     *                              PORTD 
     * RD11 <- N/C     | RD10 -> Dis_CS   | RD9  -> Dis_RES  | RD8  -> RTCC
     * RD7  <- N/C     | RD6  <- N/C      | RD5  -> Dis_R/W  | RD4  -> Dis_E
     * RD3  <- N/C     | RD2  <- N/C      | RD1  <- N/C      | RD0  <- N/C
     */
    ANSELD = 0b0000100011001111;
    TRISD  = 0b0000100011001111;
    LATD   = 0b0000000000000000;
    
    /*********************************************************************** 
     *                              PORTE 
     * RE7:0 -> Dis_D7:0
     */
    ANSELE = 0b0000000000000000;
    TRISE  = 0b0000000000000000;
    LATE   = 0b0000000000000000;
    
    /***********************************************************************
     *                              PORTF
     * RF6 <- ACC_INT1 | RF5 <- ACC_INT2  | RF4 -> ACC_RST   | RF3 -> Bst_En
     * RF2 <- REG_nPG  | RF1 <- REG_STAT1 | RF0 <- REG_STAT2 
     */
    ANSELF = 0b0000000000000000;
    TRISF  = 0b0000000001100111;
    LATF   = 0b0000000000000000;
    
    /***********************************************************************
     *                              PORTG
     * RG9 <- SW1      | RG8 <- SW2       | RG7 <- SW3       | RG6 <- SW4
     * RG3 <- SDA      | RG2 <- SCL
     */ 
    ANSELG = 0b0000000000000000;
    TRISG  = 0b0000001111001100;
    LATG   = 0b0000000000000000;
};